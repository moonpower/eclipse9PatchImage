package imagebuttonpatch.model;

public class ContentData {
	private int startX;
	private int startY;
	private int endX;
	private int endY;
	private int width;
	private int height;

	public int getStartX() {
		return startX;
	}

	public void setStartX(int x) {
		this.startX = x;
	}

	public int getStartY() {
		return startY;
	}

	public void setStartY(int y) {
		this.startY = y;
	}

	public int getWidth() {
		return width;
	}

	public void setWidth(int width) {
		this.width = width;
	}

	public int getHeight() {
		return height;
	}

	public void setHeight(int height) {
		this.height = height;
	}

	public int getEndX() {
		return endX;
	}

	public void setEndX(int endX) {
		this.endX = endX;
	}

	public int getEndY() {
		return endY;
	}

	public void setEndY(int endY) {
		this.endY = endY;
	}

}
